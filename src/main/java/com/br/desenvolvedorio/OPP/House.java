package com.br.desenvolvedorio.OPP;

public class House {

    public int lenghtM2;
    public float value;
    public int floors;
    public int vacanciesNumber;

    public int getLenghtM2() {
        return lenghtM2;
    }

    public void setLenghtM2(int lenghtM2) {
        this.lenghtM2 = lenghtM2;
    }

    public float getValue() {
        return value;
    }

    public void setValue(float value) {
        this.value = value;
    }

    public int getFloors() {
        return floors;
    }

    public void setFloors(int floors) {
        this.floors = floors;
    }

    public int getVacanciesNumber() {
        return vacanciesNumber;
    }

    public void setVacanciesNumber(int vacanciesNumber) {
        this.vacanciesNumber = vacanciesNumber;
    }
}
